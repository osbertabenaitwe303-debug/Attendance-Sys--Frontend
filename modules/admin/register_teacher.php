if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $name = trim($_POST['name']);
    $email = trim($_POST['email']);
    $password = $_POST['password'];
    $phone = trim($_POST['phone']);
    $teacherCode = trim($_POST['teacher_code']);
    
    // Validate all fields
    $nameValidation = validateName($name);
    $emailValidation = filter_var($email, FILTER_VALIDATE_EMAIL);
    $passwordValidation = validatePassword($password);
    $phoneValidation = validatePhone($phone);
    $codeValidation = validateTeacherCode($teacherCode);
    
    if ($nameValidation !== true) {
        $error = $nameValidation;
    } elseif (!$emailValidation) {
        $error = "Invalid email format. Please enter a valid email address.";
    } elseif ($passwordValidation !== true) {
        $error = $passwordValidation;
    } elseif ($phoneValidation !== true) {
        $error = $phoneValidation;
    } elseif ($codeValidation !== true) {
        $error = $codeValidation;
    } else {
        // All validations passed, proceed with registration
        $result = $userModel->addTeacher($name, $email, $password, $teacherCode, $phone);
        
        if ($result) {
            // Get the newly created teacher ID
            $database = new Database();
            $conn = $database->getConnection();
            $query = "SELECT t.id FROM teachers t JOIN users u ON t.user_id = u.id WHERE u.email = :email";
            $stmt = $conn->prepare($query);
            $stmt->bindParam(':email', $email);
            $stmt->execute();
            $teacher = $stmt->fetch(PDO::FETCH_ASSOC);
            $teacher_id = $teacher['id'];
            
            $message = "✅ Teacher registered successfully! Teacher Code: " . $teacherCode;
            
            // If face enrollment is selected, redirect to enrollment
            if (isset($_POST['enroll_face']) && $_POST['enroll_face'] == 'yes') {
