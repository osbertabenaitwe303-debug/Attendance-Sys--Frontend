function validateName($name) {
    // Allow only letters A-Z, a-z, spaces, and hyphens
    if (!preg_match('/^[A-Za-z\s\-\.]+$/', $name)) {
        return "Name should only contain letters (A-Z), spaces, hyphens, or periods";
    }
    if (strlen($name) < 3) {
        return "Name must be at least 3 characters long";
    }
    return true;
}

function validatePassword($password) {
    // Password must contain letters AND numbers, minimum 5 characters
    if (strlen($password) < 5) {
        return "Password must be at least 5 characters long";
    }
    if (!preg_match('/[A-Za-z]/', $password)) {
        return "Password must contain at least one letter (A-Z)";
    }
    if (!preg_match('/[0-9]/', $password)) {
        return "Password must contain at least one number (0-9)";
    }
    return true;
}

function validatePhone($phone) {
    if (empty($phone)) {
        return true; // Phone is optional
    }
    // Remove any non-digit characters for validation
    $cleanPhone = preg_replace('/[^0-9]/', '', $phone);
    if (strlen($cleanPhone) != 13) {
        return "Phone number must be exactly 13 digits (e.g., 256789123456)";
    }
    return true;
}

function validateTeacherCode($code) {
    // Allow letters A-Z and numbers, minimum 3 characters
    if (!preg_match('/^[A-Za-z0-9]+$/', $code)) {
        return "Teacher code should only contain letters (A-Z) and numbers (0-9)";
    }
    if (strlen($code) < 3) {
        return "Teacher code must be at least 3 characters long";
    }
    return true;
}

